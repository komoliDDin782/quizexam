let currentQuestionIndex = 0;
let score = 0;

// Shuffle function to randomize options
function shuffle(array) {
  const arr = array.slice();
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

function showQuestion() {
  const questionObj = questions[currentQuestionIndex];
  document.getElementById('question').textContent = `Вопрос ${currentQuestionIndex + 1}: ${questionObj.question}`;

  const shuffledOptions = shuffle(questionObj.options);
  const optionsDiv = document.getElementById('options');
  optionsDiv.innerHTML = ""; // Clear previous options

  shuffledOptions.forEach(optionText => {
    const btn = document.createElement('button');
    btn.textContent = optionText;
    btn.classList.add('option-btn');

    btn.onclick = () => {
      // Disable all buttons
      const buttons = document.querySelectorAll('.option-btn');
      buttons.forEach(b => b.disabled = true);

      const correctAnswer = questionObj.options[0];

      if (optionText === correctAnswer) {
        btn.classList.add('correct');
        score++;
      } else {
        btn.classList.add('incorrect');
        // Highlight the correct button
        buttons.forEach(b => {
          if (b.textContent === correctAnswer) {
            b.classList.add('correct');
          }
        });
      }

      // Go to next question after short delay
      setTimeout(() => {
        currentQuestionIndex++;
        if (currentQuestionIndex < questions.length) {
          showQuestion();
        } else {
          showResult();
        }
      }, 1000); // 1 second delay
    };

    optionsDiv.appendChild(btn);
  });

  document.getElementById('progress').textContent = `Вопрос ${currentQuestionIndex + 1} из ${questions.length}`;
}

function showResult() {
  const container = document.getElementById('quiz-container');
  container.innerHTML = `<h2>Тест завершён!</h2>
                         <p>Ваш результат: ${score} из ${questions.length}</p>`;
}

// Start quiz
showQuestion();
